public class OperatorsFunction {
    public static void main(String [] args ){
        int a=10;
        int b=15;
        int sum=a+b;
        int diff=a-b;
        int mul=a*b;
        int div=a/b;
        int rem=a%b;

        System.out.println("The sum of a and b"+ sum);
        System.out.println("The Difference of a and b"+ diff);
        System.out.println("The multiply of a and b"+ mul);
        System.out.println("The Division of a and b"+div);
        System.out.println("The remainder of a and b"+rem);


    }
    
}
